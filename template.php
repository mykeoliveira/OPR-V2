                        <p align="justify">O estilos cognitivos, também conhecidos como estilos de aprendizagem, são definidos pelas características do usuário, ou seja, seu comportamento, modo de pensar, resolver problemas, executar tarefas, preferências, etc.  </p>
                        <p align="justify">O objeto também considera o nível de conhecimento que o usuário tem sobre o assunto tratado. Os níveis estão divididos entre básico, médio e avançado.  </p>
