             <h2>Instruções</h2>
                    
                    <div class="line"></div>   
            
				<p align="justify">Para ter acesso ao recurso, o primeiro passo a ser seguido é o cadastro do seu usuário no OPR. Para isso, basta clicar no botão "Log in" que está localizado na barra de menu no canto superior direito da tela.</p> <br />
                <p align="center"><img src="images/pic1.png" width="594" height="336" /></p><br />
                
                <p align="justify">Na página de Log in, é preciso clicar em "Sign Up", localizado abaixo da caixa de log in, como é ilustrado na seguinte imagem. Se já tiver cadastro, basta preencher os campos de "Username" e "Password" para poder ter acesso ao recurso. </p><br /><br />
                <p align="center"><img src="images/cadastrar.png" width="594" height="336" /></p><br />
        
        		<p align="justify">Para o cadastro, são solicitados o nome, um nome de usuário e uma senha. Então, basta preencher todos os campos solicitados. Em seguida, clique em "Register", como mostrado na figura a seguir. O botão "Clean" server para limpar os espaços de preenchimento do cadastro, e o usuário pode voltar à página anterior clicando em "Return", localizado no canto esquerdo.</p><br /> <br />
                <p align="center"><img src="images/cadastro.png" width="500" height="300" /></p><br />
        
                <p align="justify">Quando o usuário estiver cadastrado, ele será direcionado para a página de login novamente, para então poder acessar o recurso. <br />
                Quando o usuário já estiver logado, ele será direcionado para o primeiro questionário, o qual avalia o estilo cognitivo do aluno. É necessário responder todas as 18 questões solicitadas. Se alguma questão ficar em branco, o usuário poderá voltar e responder a questão em branco sem ter que recomeçar o questionário.  </p><br /><br />
                <p align="center"><img src="images/questionario_estilos.png" width="594" height="336" /></p><br />
        
                <p align="justify"> O resultado do questionário aparecerá assim que o mesmo estiver respondido. A imagem abaixo mostra um exemplo de resultado, podendo ser holista, serialista, divergente, convergente, impulsivo e reflexivo. Para ir para a próxima etapa, clique em "here", como ilustrado na figura abaixo.</p> <br /><br />
                <p align="center"><img src="images/resultado_estilo.png" width="594" height="200" /></p><br />
        
                <p align="justify">Na próxima etapa, o usuário receberá questões para avaliar o seu nível de conhecimento. São 12 questões envolvendo o conteúdo introdutório de programação, sendo elas separadas umas das outras. É importante que o usuário clique primeiramente no botão "Verificar" (Circulado com vermelho), senão o sistema não avalia a pontuação da questão. Em seguida, o usuário clica em "Próximo" para poder seguir para a próxima questão. Obs: A pontuação aparece em porcentagem. </p><br /><br />
                <p align="center"><img src="images/questionario_nivel_conhecimento.png" width="594" height="336" /></p><br />

                <p align="justify">Após isso, o usuário será direcionado para exercícios no seu nível de conhecimento, e terá acesso a materiais educacionais com diferentes formatos de acordo com o seu estilo cognitivo, como pode ser visualizado na figura a seguir. </p><br /><br />
                <p align="center"><img src="images/materiais.png" width="594" height="336" /></p><br />

                <p align="justify">O exemplo a seguir ilustra um usuário Divergente Avançado. Existem materiais para Fluxograma e Pseudocódigo, VisualG, Teste de Mesa e Linguagem C. O tipo de material indicado para esse perfil é tutorial com Diagramas.</p><br /><br />
                <p align="center"><img src="images/formatos.png" width="594" height="336" /></p><br />
        
        
        
        
        
        	
        
        
